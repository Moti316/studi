/**
 * בונה את ה-prompt לחיבור תרחיש בודד: זרע + עיגון-חקיקה-מאומת → JSON 4-חלקים.
 * **מגביל** את המודל לשמר את הציטוטים-המאומתים (לא להמציא תקנות).
 */
export function buildScenarioAuthoringPrompt(
  seed: ScenarioSeed,
  grounding: GroundedCitation[],
): string {
  const groundingBlock =
    grounding.length > 0
      ? grounding
          .map(
            (g) =>
              `- scope ${g.scopeId} · ${g.statuteTitle} · ${g.section}\n  ציטוט-מאומת: "${g.quote}"`,
          )
          .join('\n')
      : '(אין עיגון — אם אינך בטוח בתקנה, תייג [לא ידוע] ואל תמציא.)';

  return [
    `חבר תרחיש-וועדה מלא מהזרע הבא, מעוגן אך-ורק בעיגון-החקיקה שסופק.`,
    '',
    `כותרת: ${seed.title}`,
    `רקע: ${seed.background}`,
    `משימה: ${seed.task}`,
    '',
    'עיגון-חקיקה מאומת (השתמש בו לגיבוי-החוקי · אל תוסיף תקנות שלא כאן):',
    groundingBlock,
    '',
    'החזר JSON בלבד (ללא fences):',
    '{"title","immediateAction","controlsHierarchy","legalBackup","legalCitation":{"scopeId","quote","section"},"managerialAction"}',
    'legalCitation.quote = העתק מילולי מהעיגון-שסופק. controlsHierarchy = מדרג-הבקרות (צמ"א אחרון).',
  ].join('\n');
}
