/**
 * ארבעת שלבי-ה-wizard של פרויקט-הגמר:
 *   site     — בחירת-אתר + פרופיל-מקום-עבודה (שלב 1).
 *   hazards  — סקר-מפגעים + בניית טבלת-JSA שורה-אחר-שורה (שלבים 2-3).
 *   matrix   — הצגת מטריצת-הסיכון + מדרג-בקרות (שלבים 4-5).
 *   feedback — משוב-AI על העבודה + הכנה להגשה (שלב 6).
 */
export type CapstoneStep = 'site' | 'hazards' | 'matrix' | 'feedback';

// ---------------------------------------------------------------------------
// פרופיל-האתר (שלב 1)
// ---------------------------------------------------------------------------

/**
 * ענפי-תעשייה הרלוונטיים לעבודת-גמר בבטיחות תעסוקתית.
 * מבוסס על פרקי-הקורס ד-ח (חשמל / חומ"ס / גובה-ובנייה / חקלאות / תעשייה-כללית).
 */
export type IndustrySector =
  | 'construction' // בנייה
  | 'manufacturing' // ייצור / מפעל
  | 'electrical' // חשמל
  | 'chemicals' // חומרים מסוכנים (חומ"ס)
  | 'agriculture' // חקלאות
  | 'logistics' // לוגיסטיקה / מחסנאות
  | 'maintenance' // אחזקה
  | 'other'; // אחר

/**
 * פרופיל-מקום-העבודה שהמשתמש מזין בשלב 1.
 * הנתונים אמיתיים (דרישת-משרד-העבודה: אתר-ממשי בלבד).
 */
export interface SiteInfo {
  /** שם-תיאורי (לא PII — שם-ענף/תפקוד, לא שם-אדם/חברה). */
  name: string;
  /** ענף-הפעילות (מחולל-השדות-ב-wizard). */
  sector: IndustrySector;
  /** מספר-עובדים משוער (לכיול-הסיכון וההמלצות). */
  workerCount: number;
  /**
   * מפגעים-עיקריים שנצפו בשטח (מזוהים בסיור — לא מהזיכרון).
   * מחרוזות-חופשיות שהמשתמש מזין; המערכת מעשירה מה-gaps-check.
   */
  mainHazards: string[];
}

// ---------------------------------------------------------------------------
// שורת-JSA (שלב 2-3)
// ---------------------------------------------------------------------------

/**
 * ערכי-חומרה (1-4) — ממקרא-המשרד:
 *   1 = מזערי (ללא פציעה / נזק-זניח)
 *   2 = קל    (פציעה-קלה / נזק-מינורי)
 *   3 = בינוני (פציעה-משמעותית / נזק-בינוני)
 *   4 = חמור  (נכות-קבועה / פטירה / נזק-כבד)
 */
export type SeverityLevel = 1 | 2 | 3 | 4;

/**
 * ערכי-סבירות (1-4) — ממקרא-המשרד:
 *   1 = נמוכה-מאוד (מוצדק: כמעט-לא קורה)
 *   2 = נמוכה       (קורה לעיתים-נדירות)
 *   3 = בינונית      (קורה לפעמים)
 *   4 = גבוהה        (קורה לעיתים-קרובות)
 */
export type ProbabilityLevel = 1 | 2 | 3 | 4;

/**
 * שורה בודדת בטבלת-JSA (6 עמודות + מזהה).
 * כל שדה מקביל לעמודה ב"פורמט-החובה" של עדכון-תכנית 19.10.2025.
 *
 * name-clean: `owner` = תיאור-תפקיד בלבד (לא שם-אדם).
 */
export interface JsaRow {
  /** מזהה-ייחודי (nanoid / uuid). */
  id: string;

  // עמודה 1 — גורם-הסיכון
  /** תיאור-מפגע / גורם-הסיכון (מה עלול לפגוע). */
  hazard: string;

  // עמודה 2 — תרחיש-להתממשות
  /** כיצד הסיכון מתממש (מנגנון-נזק). */
  scenario: string;

  // עמודה 3 — בקרות-קיימות
  /**
   * בקרות-קיימות (לפי מדרג-הבקרות — מהנדסיות/מנהלתיות/צמ"א).
   * חייב לפרט את-רמת-הבקרה (לא רק "כפפות").
   */
  existingControls: string;

  // עמודות 4a + 4b — הערכת-סיכון
  /** חומרה 1-4 (לפי מקרא-המשרד). */
  severity: SeverityLevel;
  /** סבירות 1-4 (לפי מקרא-המשרד). */
  probability: ProbabilityLevel;

  // עמודה 5 — בקרות-נוספות-נדרשות
  /**
   * המלצות-בקרה נוספות לפי מדרג (חיסול → החלפה → הנדסי → מנהלי → צמ"א).
   * הוולידציה (validateHierarchy) מאמתת שלא ממליצים על צמ"א
   * כאשר בקרה-הנדסית אפשרית.
   */
  addedControls: string;

  // עמודה 6 — אחראי ומועד
  /** תפקיד-אחראי-לביצוע (שם-תפקיד בלבד, לא שם-אדם — name-clean). */
  owner: string;
  /**
   * מועד-ביצוע-יעד (ISO date string "YYYY-MM-DD" · ריק = לא-הוגדר).
   * המשתמש ממלא; לא-חובה בשלב-המזוין.
   */
  due: string;
}

// ---------------------------------------------------------------------------
// מטריצת-הסיכון (חישוב · שלב 3)
// ---------------------------------------------------------------------------

/**
 * ציון-סיכון מספרי = חומרה × סבירות (טווח: 1-16).
 *
 * @param severity  1-4
 * @param probability 1-4
 * @returns ציון 1-16
 */
export function riskLevel(severity: SeverityLevel, probability: ProbabilityLevel): number {
  return severity * probability;
}

/**
 * סיווג-רצועה לפי מקרא-המשרד (מסמך-JSA הרשמי):
 *   green  — 1-4  : ליבק (טיפול-שגרתי)
 *   yellow — 6-9  : לא-ליבק (להפחית; המשך-זמני רק אם הכרחי)
 *   red    — 12-16: לא-ליבק (פעולה-מיידית; עד-עצירת-עבודה)
 *
 * ציון 5 ו-10-11 אינם ברצועה-מוגדרת במקרא-הרשמי;
 * מטופלים ב-best-fit (5→yellow · 10-11→red) כי המשרד מגדיר
 * "כל מה שמעל 4 — אינו ליבק".
 *
 * @param score ציון מ-riskLevel()
 */
export function riskBand(score: number): 'green' | 'yellow' | 'red' {
  if (score <= 4) return 'green';
  if (score <= 9) return 'yellow';
  return 'red';
}

