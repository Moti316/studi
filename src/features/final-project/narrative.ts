export function isValidNarrative(x: unknown): x is Omit<ProjectNarrative, 'source'> {
  if (!x || typeof x !== 'object') return false;
  const n = x as Record<string, unknown>;
  const keys: NarrativeChapterKey[] = [
    'aboutCompany',
    'aboutProject',
    'orgStructure',
    'workProcesses',
    'riskAnalysis',
  ];
  return keys.every((k) => typeof n[k] === 'string' && (n[k] as string).trim().length >= 40);
}

