/** מסכם את טבלת-ה-JSA לפרומפט (כדי שפרקי-הסיכונים יתייחסו לממצאים-בפועל). */
function summarizeRows(rows: JsaRow[]): string {
  if (rows.length === 0) return '(טרם נבנו שורות-JSA)';
  const reds = rows.filter((r) => riskBand(assessmentScore(r.riskBefore)) === 'red').length;
  const yellows = rows.filter((r) => riskBand(assessmentScore(r.riskBefore)) === 'yellow').length;
  const top = rows
    .slice(0, 10)
    .map((r, i) => `${i + 1}. ${r.hazard} (סיכון לפני: ${assessmentScore(r.riskBefore)})`)
    .join('\n');
  return `סה"כ ${rows.length} שורות · ${reds} אדומות · ${yellows} צהובות.\n${top}`;
}

/** בלוק פרופיל-האתר — חוזר בכל פרומפט-פרק. */
function siteProfileBlock(site: SiteInfo): string {
  return (
    `## פרופיל-האתר (אמיתי · נסקר ע"י הלומד)\n` +
    `שם/תיאור: "${site.name}"\n` +
    `ענף: ${SECTOR_HE[site.sector]} (${site.sector})\n` +
    `מספר-עובדים: ${site.workerCount}\n` +
    `מפגעים-עיקריים שצוינו: ${site.mainHazards.length ? site.mainHazards.join(' · ') : '(לא צוינו)'}`
  );
}

/** עוטף הנחיית-פרק ל-system-prompt מלא. */
function chapterSystem(instruction: string): string {
  return `${AUTHOR_PREAMBLE}\n\n${instruction}\n\n${STYLE_RULES}\n\n${OUTPUT_RULES}`;
}

