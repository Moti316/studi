// fallback דטרמיניסטי (ללא-Claude) — נוסח-מסגרת מקצועי שלם (לא stub)
// ---------------------------------------------------------------------------

/** מרכיב נרטיב-דטרמיניסטי מלא מ-fallback פר-פרק (source='deterministic'). */
export function buildDeterministicNarrative(site: SiteInfo, rows: JsaRow[]): ProjectNarrative {
  const byKey = (k: NarrativeChapterKey): string => {
    const spec = NARRATIVE_CHAPTERS.find((c) => c.key === k);
    return spec ? spec.deterministic(site, rows) : '';
  };
  return {
    aboutCompany: byKey('aboutCompany'),
    aboutProject: byKey('aboutProject'),
    orgStructure: byKey('orgStructure'),
    workProcesses: byKey('workProcesses'),
    riskAnalysis: byKey('riskAnalysis'),
    source: 'deterministic',
  };
}
