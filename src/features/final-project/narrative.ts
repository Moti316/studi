/** חמשת הפרקים-הנרטיביים (פרק-5 = הטבלה · נפרד). כל שדה = פרוזה-עברית רב-פסקאות. */
export interface ProjectNarrative {
  /** פרק 1 — אודות החברה (פרופיל: תחום, גודל, מאפיינים). */
  aboutCompany: string;
  /** פרק 2 — אודות הפרויקט ותהליכי-העבודה (מה מבוצע באתר). */
  aboutProject: string;
  /** פרק 3 — מבנה ארגוני (תפקידים + קווי-דיווח · name-clean). */
  orgStructure: string;
  /** פרק 4 — פירוט תהליכי-עבודה וסיכונים-כלליים. */
  workProcesses: string;
  /** פרק 6 — ניתוח-סיכונים (סיכום ממצאי-הטבלה · מדרג-בקרות · המלצות). */
  riskAnalysis: string;
  source: 'claude' | 'deterministic';
}

